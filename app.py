# 3. CORE LOGIC / RAG INITIALIZATION ENGINE
# ==========================================
@st.cache_resource(show_spinner=False)
def initialize_rag_system(api_key, url):
    """Downloads content, splits into chunks, creates semantic embeddings, and initializes Chroma DB."""
    try:
        # Step 1: Document Fetching
        loader = UnstructuredURLLoader(urls=[url])
        documents = loader.load()

        # Step 2: Content Splitting
        text_splitter = RecursiveCharacterTextSplitter(chunk_size=1000, chunk_overlap=200)
        chunks = text_splitter.split_documents(documents)

        # Step 3: Vectorization & Storage
        embeddings = OpenAIEmbeddings(openai_api_key=api_key)
        vectorstore = Chroma.from_documents(chunks, embeddings)
        return vectorstore.as_retriever(search_kwargs={"k": 4})
    except Exception as e:
        st.error(f"Failed to process RAG Pipeline: {str(e)}")
        return None

