if user_query := st.chat_input("Ask me about insurance policies, coverage parameters, or benefits..."):

    # 1. Print Query to View Screen
    with st.chat_message("user"):
        st.markdown(user_query)
    st.session_state.messages.append({"role": "user", "content": user_query})

    # 2. Extract Document Context via Vector DB
    retrieved_docs = retriever.invoke(user_query)
    context_blob = "\n\n".join([doc.page_content for doc in retrieved_docs])

    # 3. Assemble Conversational Chain Models
    prompt_template = ChatPromptTemplate.from_messages([
        ("system", "You are an expert context-aware assistant for Star Health Insurance. "
                   "Provide clear, informative, and precise guidance to patients using the extracted "
                   "knowledge base details. If the answer cannot be confidently deduced from the text manual, "
                   "advise the user to contact Star Health Support directly.\n\n"
                   "Manual Context Extracts:\n{context}"),
        MessagesPlaceholder(variable_name="history"),
        ("human", "{question}")
    ])

    llm = ChatOpenAI(model="gpt-4o-mini", temperature=0.3, openai_api_key=openai_api_key)
    brain_chain = prompt_template | llm

    # Wrap with contextual message history constraints
    conversational_agent = RunnableWithMessageHistory(
        brain_chain,
        lambda session_id: st.session_state.chat_history,
        input_messages_key="question",
        history_messages_key="history"
    )

    # 4. Process Generation Request
    with st.chat_message("assistant"):
        with st.spinner("Reviewing insurance documentation frameworks..."):
            response = conversational_agent.invoke(
                {"context": context_blob, "question": user_query},
                config={"configurable": {"session_id": "default_user_session"}}
            )
            response_text = response.content
            st.markdown(response_text)

            # 5. Process Integrated Audio Generation Engine (TTS)
            message_store = {"role": "assistant", "content": response_text}

            if enable_tts:
                try:
                    tts = gTTS(text=response_text, lang='en', slow=False)
                    audio_buffer = BytesIO()
                    tts.write_to_fp(audio_buffer)
                    audio_bytes = audio_buffer.getvalue()

                    st.audio(audio_bytes, format="audio/mp3")
                    message_store["audio"] = audio_bytes
                except Exception as audio_err:
                    st.caption("🔊 Audio synthesis engine encountered formatting delays.")

            st.session_state.messages.append(message_store)
